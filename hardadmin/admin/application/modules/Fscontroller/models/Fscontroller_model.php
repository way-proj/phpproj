	public function checkDataById($table,$whereAssoc){
		$this->db->where($whereAssoc);
		$query = $this->db->get($table);
		return $query->num_rows();
	}
	
	public function getSignleRow($table,$whereAssoc){
	   $this->db->where($whereAssoc);
	   $query = $this->db->get($table);
	   return $query->row_array();
	}
	
	public function getAllRows($table){
	   $query = $this->db->get($table);
	   return $query->result_array();
	}
	
	public function getAllRowsOrderBy($table,$orderbyColumn){
		$this->db->order_by($orderbyColumn);
	   $query = $this->db->get($table);
	   return $query->result_array();
	}
	
	public function getRows($table,$whereAssoc){
	   $this->db->where($whereAssoc);
	   $query = $this->db->get($table);
	   return $query->result_array();
	}
	
	public function deleteRows($table,$whereAssoc){
